/**
 * Get career by ID
 */
export function getCareerById(id: string): CareerData | undefined {
  return careerDatabase.find(c => c.id === id);
}

/**
 * Get careers by category
 */
export function getCareersByCategory(category: string): CareerData[] {
  return careerDatabase.filter(c => c.category.toLowerCase().includes(category.toLowerCase()));
}

/**
 * Get all unique categories
 */
export function getCategories(): string[] {
  return Array.from(new Set(careerDatabase.map(c => c.category)));
}
